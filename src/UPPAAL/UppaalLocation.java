package UPPAAL;

public class UppaalLocation {
	Boolean init=false;

	String Name="null";
	int id;	
	double R1;
	double R2;
	double Energe;
	double T1;
	double T2;
	public Boolean getInit() {
		return init;
	}
	public void setInit(Boolean init) {
		this.init = init;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getR1() {
		return R1;
	}
	public void setR1(double r1) {
		R1 = r1;
	}
	public double getR2() {
		return R2;
	}
	public void setR2(double r2) {
		R2 = r2;
	}
	public double getEnerge() {
		return Energe;
	}
	public void setEnerge(double energe) {
		Energe = energe;
	}
	public double getT1() {
		return T1;
	}
	public void setT1(double t1) {
		T1 = t1;
	}
	public double getT2() {
		return T2;
	}
	public void setT2(double t2) {
		T2 = t2;
	}
	
	

}
